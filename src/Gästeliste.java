public class Gästeliste {
    public static void main(String[] args) {
       int gäste = 5;

       gäste ++;  // gäste = gäste + 1;
       gäste --; // gäste = gäste - 1;
        gäste += 5; // gäste = gäste + 5;
        gäste -= 5; // gäste = gäste - 5;

         System.out.println("Es sind " + gäste + " Gäste eingeladen.");
    }
}
